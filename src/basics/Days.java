package basics;

public class Days {

	public static void main(String[] args) {

		String daysOfWeek = "Вторник";

		switch (daysOfWeek) {
		case "Понедельник":
			System.out.println("Понедельник");
			break;
		case "Вторник":
			System.out.println("Вторник");
			break;
		case "Среда":
			System.out.println("Среда");
			break;
		case "Четверг":
			System.out.println("Четверг");
			break;
		case "Пятница":
			System.out.println("Пятница");
			break;
		}

	}

}
